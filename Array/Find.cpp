
#include<iostream>
using namespace std;

int main() {
// linear search
int n = 10;
int a[n] = { 10, 20, 30, 40, 50 };
int totalElements = 5, valueToFind = 50, index = -1;
for(int i=0; i<totalElements; i++) {
if(a[i] == valueToFind) {
index = i;
break;
}
}
if(index != -1) {
cout << "Element found at index " << index << "\n";
}else {
cout << "Element NOT FOUND!!\n";
}
return 0;
}
